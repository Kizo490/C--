#include <iostream>
#include <cmath>

using namespace std;

int main(){
    cout << "Podstawowe operacje matematyczne ktore mozna wykonywac za pomoca samego cout: " << endl;
    cout << endl;
    cout << 10 + 5 << endl;
    cout << 10 - 5 << endl;
    cout << 10 + 5 * 10 << endl;
    cout << (10 + 5) * 10 << endl;
    cout << 10 / 3 << endl;
    cout << 10.0 / 3.0 << endl;

    cout << pow(3 , 2) << endl;
    cout << sqrt(36) << endl;
    cout << round(6.7) << endl;
    cout << ceil(4.1) << endl;
    cout << fmax(3, 10) << endl;



    return 0;
}